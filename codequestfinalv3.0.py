# TYPEWRITER EFFECT FUNCTION
def message(
    string, delay=0.004, end_char="\n"
):  # 2nd para use for time delays / optional para set, but can be changed
    print(
        end=end_char
    )  # end_char overwrites end default (which is end = /n) - when end_char is changed in the code so is end

    for x in string:

        # messaging each character of the message
        print(x, end="")

        # adding time delay of set value
        time.sleep(delay)

    print(end=end_char)


